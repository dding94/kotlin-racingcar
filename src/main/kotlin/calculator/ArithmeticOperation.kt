package calculator

class ArithmeticOperation {
    fun addition(a: Double, b: Double) = a + b

    fun subtraction(a: Double, b: Double) = a - b

    fun multiplication(a: Double, b: Double) = a * b

    fun division(a: Double, b: Double): Double {
        if (b == Number.ZERO) {
            throw ArithmeticException()
        }

        return a / b
    }
}
